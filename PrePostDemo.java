
public class PrePostDemo {

	public static void main(String[] args) {
		
				int i = 3;

				i++;
		        //increase by 1
				System.out.println(i);
		        // prints 4
			        
		        ++i;			   
		        //increase by 1
		        System.out.println(i);
		        // prints 5
		        
		        //increase by 1 and print at the same time
		        System.out.println(++i);
		        // prints 6
		        
		        System.out.println(i++);
		        //print current i and increase by 1 after the print out - just for the next time
		        
		        // prints 7
		        System.out.println(i);
		        
    }
}

